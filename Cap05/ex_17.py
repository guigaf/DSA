# Crie uma lista vazia e uma variável com valor 4. 
# Enquanto o valor da variável for menor ou igual a 20 
# Adicione à lista, apenas os valores pares e imprima a lista

emptyList = []
arr1 = 4
while arr1 <= 20:
    emptyList.append(arr1)
    arr1 += 1
print("O conteudo da lista é: ", emptyList)